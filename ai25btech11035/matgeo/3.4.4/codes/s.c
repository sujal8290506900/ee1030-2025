#include <stdio.h>

int main() {
    // Rectangle vertices
    int Ax = 0, Ay = 0;
    int Bx = 0, By = 5;
    int Cx = 3, Cy = 5;
    int Dx = 3, Dy = 0;

    printf("Coordinates of the rectangle are:\n");
    printf("A(%d, %d)\n", Ax, Ay);
    printf("B(%d, %d)\n", Bx, By);
    printf("C(%d, %d)\n", Cx, Cy);
    printf("D(%d, %d)\n", Dx, Dy);

    return 0;
}
